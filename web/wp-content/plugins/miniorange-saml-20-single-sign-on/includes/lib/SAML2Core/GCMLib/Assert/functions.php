<?php


namespace Assert;

function that($g2, $CB = null, $u6 = null)
{
    return Assert::that($g2, $CB, $u6);
}
function thatAll($aq, $CB = null, $u6 = null)
{
    return Assert::thatAll($aq, $CB, $u6);
}
function thatNullOr($g2, $CB = null, $u6 = null)
{
    return Assert::thatNullOr($g2, $CB, $u6);
}
function lazy()
{
    return Assert::lazy();
}
